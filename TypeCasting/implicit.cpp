#include <iostream>
using namespace std;

int main() {
    // 1. int to float
    int num1 = 10;
    float num2 = 5.5;
    float result = num1 + num2;
    cout << result << endl;

    // 2. char to int
    char ch = 'A';
    int a = ch + 1;
    cout << a << endl;

    // 3. int to char
    int alphabet = 97;
    char ch2 = alphabet;
    cout << ch2 << endl;

    return 0;
}